# !统计逗号个数

# 高橋君は整数を書くとき、下から 3 桁ごとにコンマで区切って書きます。
# 例えば 1234567 であれば 1,234,567、777 であれば 777 と書きます。
# 高橋君が 1 以上 N 以下の整数を 1 度ずつ書くとき、コンマは合計で何回書かれますか？
# !1<n=<=1e15


# 1-999 0
# 1000 - 999,999 1
# 1,000,000 - 999,999,999 2
# 1,000,000,000 - 999,999,999,999 3
# 1,000,000,000,000 - 999,999,999,999,999 4
# 1,000,000,000,000,000 - 999,999,999,999,999,999 5
# 1,000,000,000,000,000,000 - 999,999,999,999,999,999,999 6
# 1,000,000,000,000,000,000,000 - 999,999,999,999,999,999,999,999 7
def comma(n: int) -> int:
    res = 0
    if n >= 1_000:
        res += n - 999
    if n >= 1_000_000:
        res += n - 999_999
    if n >= 1_000_000_000:
        res += n - 999_999_999
    if n >= 1_000_000_000_000:
        res += n - 999_999_999_999
    if n >= 1_000_000_000_000_000:
        res += n - 999_999_999_999_999
    if n >= 1_000_000_000_000_000_000:
        res += n - 999_999_999_999_999_999
    if n >= 1_000_000_000_000_000_000_000:
        res += n - 999_999_999_999_999_999_999
    return res


n = int(input())
print(comma(n))
