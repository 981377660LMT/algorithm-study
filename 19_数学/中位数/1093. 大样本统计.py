from typing import List
from bisect import bisect_right, bisect_left

# 我们对 0 到 255 之间的整数进行采样，并将结果存储在数组 count 中：
# count[k] 就是整数 k 的`采样个数`。(计数排序结果)
# 分别返回样本的最小值、最大值、平均值、中位数和众数。其中，众数是保证唯一的。


class Solution:
    def sampleStats(self, count: List[int]) -> List[float]:
        n = sum(count)
        mi = next(i for i in range(256) if count[i]) * 1.0
        ma = next(i for i in range(255, -1, -1) if count[i]) * 1.0
        mean = sum(i * v for i, v in enumerate(count)) * 1.0 / n
        mode = count.index(max(count)) * 1.0

        for i in range(255):
            count[i + 1] += count[i]
        median1 = bisect_right(count, (n - 1) >> 1)
        median2 = bisect_right(count, n >> 1)
        median = (median1 + median2) / 2.0
        return [mi, ma, mean, median, mode]


